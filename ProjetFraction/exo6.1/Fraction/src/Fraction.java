
public class Fraction {

	private int numerator, denominator;
	
	 public Fraction(){
	      numerator = denominator = 0;
	 }
	 public int getNumerator(){
	      return numerator;
	 }
	 public void setNumerator(int num){
	      numerator=num;
	 }
	 public int getDenominator(){
	      return denominator;
	 }
	 public void setDenominator(int den){
	      denominator=den;
	 }
	 
	 
}
